int whichBw(int ch) {
  if (ch >= 26 && ch <= 37)
    return 40;
  else if (ch >= 38  && ch <= 43)
    return 80;
  else if (ch == 44 || ch == 45)
    return 160;
  
  return 20;
}

inline int bwIdx(int bw) {
  if (bw == 40) {
    return 1;
  } else if (bw == 80) {
    return 2;
  } else if(bw == 160) {
    return 3;
  }
  return 0;
}

struct Link {
  int _idR, _idS;
  int id;
  int ch;
  int bw;
  double interference;
  double SINR;
  int MCS;

  Link() {
    _idR = _idS = id = -1;
    ch = bw = -1;
    interference = SINR = 0.0;
    MCS = -1;
  }
  
  Link(int id) : _idR(id), _idS(id), id(id) {
    //printf("entrei aqui id eh %d\n", id);
    //_idR = _idS = id = -1;
    ch = bw = -1;
    interference = SINR = 0.0;
    MCS = -1;
  }

  Link(const Link &x) {
    id = x.id;
    ch = x.ch;
    bw = x.bw;
    _idS = x._idS;
    _idR = x._idR;
    interference = x.interference;
    MCS = x.MCS;
  }

  void operator=(const Link &x) {
    id = x.id;
    ch = x.ch;
    bw = x.bw;
    interference = x.interference;
    MCS = x.MCS;
  }

  friend bool operator==(const Link &o1, const Link &o2);

  void setChannel(int ch) {
    this->ch = ch;
    this->bw = whichBw(ch);
  }
};

class Solution {
public:
  double objective;
  deque<Link> scheduled_links;

  void computeObjective() {
    
    for (Link &x : scheduled_links) {
      int mxDataRate = (x.bw == 20) ? 9 : 10;
      bool go = false;
      
      for (int _mcs = 0; _mcs < mxDataRate; _mcs++) {
        if (SINR[_mcs][bwIdx(x.bw)] > x.SINR) {
          x.MCS = _mcs - 1;
          go = true;
          
          if (x.MCS == -1) {
            x.MCS = 0;
          }
          
          break;
        }
      }

      if (!go) {
        x.MCS = mxDataRate - 1;
      }
    }

    for (Link &x : scheduled_links) {
      //printf("link %d MCS %d bw %d\n", x.id, x.MCS, x.bw);
      objective += dataRates[x.MCS][bwIdx(x.bw)];
    }
  }

  deque<Link> getScheduledLinks() const {
    return scheduled_links;
  }

  deque<Link> getLinksInChannel(int ch) const {
    deque<Link> ret;
    for (const Link &l : scheduled_links) {
      if (l.ch == ch) {
        ret.emplace_back(l);
      }
    }
    return ret;
  }

  double getObjective() {
    return objective;
  }

  void setObjective(int ob) {
    objective = ob;
  }

  void insert(const Link &l) {
    scheduled_links.emplace_back(l);
    computeInterference();

    objective = 0.0;
    computeObjective();
  }

  void computeInterference() {
    for (Link &u : scheduled_links) {
      for (Link &v : scheduled_links) {
        if (u.id == v.id) {
          //printf("     -> entrei \n");
          continue;
        }

        if (overlap[u.ch][v.ch]) {
          u.interference += interferenceMatrix[v._idR][u._idS];
        }
      }

      if (u.interference == 0.0) {
        u.SINR = 1e8;
      } else {
        u.SINR = interferenceMatrix[u._idS][u._idR] / (u.interference + noise);
      }
      
    }
  }

  void clearChannel(int ch) {
    set<int> MARK;
    for (Link &l : scheduled_links) {
      if (l.ch == ch) {
        MARK.insert(l.id);
      }
    }

    auto it = scheduled_links.begin();
    while (it != scheduled_links.end()) {
      if (MARK.count(it->id)) {
        it = scheduled_links.erase(it);
      } else {
        it++;
      }
    }
  }

  friend bool operator<(const Solution &o1, const Solution &o2);

  friend bool operator>(const Solution &o1, const Solution &o2);

  friend bool operator==(const Solution &o1, const Solution &o2);
  
  Solution &operator=(const Solution &o1);
};

